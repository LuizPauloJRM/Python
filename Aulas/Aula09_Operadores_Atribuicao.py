#= , -= , += , /= 
saldo=1000
print(f"Seu saldo em conta é de R${saldo} ")
novo_saldo=900
print(novo_saldo)
novo_saldo-=10
print(f"Voce comprou um pacote de feijao no valor de R$10,00 seu novo saldo é de {novo_saldo}")
novo_saldo+=1000
print(f"Voce depositou R$1000,00 na sua conta agora seu saldo é de R$ {novo_saldo}" )